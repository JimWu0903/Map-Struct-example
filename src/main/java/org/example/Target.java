package org.example;

public class Target {

    private String name;

    private String rocDate;
    private int age;

    // Getters and Setters


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getRocDate() {
        return rocDate;
    }

    public void setRocDate(String rocDate) {
        this.rocDate = rocDate;
    }
}




